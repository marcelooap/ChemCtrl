function countKeys(obj, prefix = '') {
  let n = 0;
  for (const [k, v] of Object.entries(obj)) {
    if (v && typeof v === 'object' && !Array.isArray(v)) {
      n += countKeys(v, prefix ? `${prefix}.${k}` : k);
    } else {
      n += 1;
    }
  }
  return n;
}

function lineCount(json) {
  return json.split('\n').length;
}

